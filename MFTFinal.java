    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int k=Integer.parseInt(jTextField1.getText());
        if(jCheckBox1.isSelected())//First Fit Case
        {
            int i;
            for(i=0;i<count;i++)
            {
                if(max[i]>=k&&oc[i]==0)
                {
                    oc[i]=k;
                    jTextArea1.setText("Process Accommodated! :)");
                    break;
                }
            }
        }
        else if(jCheckBox2.isSelected())
        {
            int i;
            int ind=-1,min=9999;
            for(i=0;i<count;i++)
            {
                if(max[i]>=k&&max[i]<min&&oc[i]==0)
                {
                    ind=i;
                    min=max[i];
                }
            }
            if(ind==-1)
                jTextArea1.setText("Process couldn't be accommodated! No free partition is big enough to hold the process!\n");
            else
            {
                oc[ind]=k;
                jTextArea1.setText("Process accommodated!\n");
            }
        }
        else
        {
            int i;
            int ind=-1,maxi=-1;
             for(i=0;i<count;i++)
            {
                if(max[i]>=k&&max[i]>maxi&&oc[i]==0)
                {
                    ind=i;
                    maxi=max[i];
                }
            }       
             if(ind==-1)
                 jTextArea1.setText("No partition big enough to hold the process!\n");
             else
             {
                 oc[ind]=k;
                 jTextArea1.setText("Process has been accommodated!\n");
             }
        
        
        }
        g.setColor(Color.magenta);
        int j;
        int total=0;
        for(j=0;j<count;j++)
        {
            if(oc[j]>0)
            {
            int m=40+total+(j*2);
            g.fillRect(100,m, 100, oc[j]);
            }
            total=total+max[j];
        }
        total=0;
        g.setColor(Color.black);
        for(j=0;j<count;j++)
                {
                    int m=40+total+(j*2);
                    if(oc[j]==0)
                    {
                       g.drawString(Integer.toString(max[j]), 100, m+10); 
                    }
                    else
                    {
                        g.drawString(Integer.toString(oc[j]), 100, m+10);
                        if(max[j]!=oc[j])
                        g.drawString(Integer.toString(max[j]-oc[j]), 100, m+oc[j]+10);
                        
                    }    
                    total=total+max[j];
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

