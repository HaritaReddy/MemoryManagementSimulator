    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(partitionarea==300)
        {
            jTextArea1.setText("Maximum space exceeded!\n");
            return;
           
        }
        int k=Integer.parseInt(jTextField3.getText());
        if(partitionarea+k>300)
        {
            jTextArea1.setText("Enter a smaller size\n");
            return;
        }
        
        count++;
        if(count%2==0)
        g.setColor(Color.yellow);
        else
        g.setColor(Color.orange);
        int m=40+partitionarea+(count-1)*2;
        g.fillRect(100,m, 100, k);
        g.setColor(Color.black);
        if(count!=1)
        g.fillRect(100,m-2,100,2);
        
        g.drawString(Integer.toString(k), 100, m+10);
        partitionarea+=k;
        max[count-1]=k;
        //g.setColor(Color.red);
        //g.fillRect(100,40, 100,350);
    }//GEN-LAST:event_jButton3ActionPerformed

